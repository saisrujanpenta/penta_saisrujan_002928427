    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
            
           int selectedRowIndex = jTable1.getSelectedRow();
           
           if (selectedRowIndex<0) {
                JOptionPane.showMessageDialog(this, "Please select a row to delete.");
                return;
                
           }
           
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           CarProfile selectedCarProfile = (CarProfile) model.getValueAt(selectedRowIndex , 0);
          
           history.deleteCarProfile(selectedCarProfile);
          
          
           JOptionPane.showMessageDialog(this, "Car Profile deleted.");
          
           populateTable();
         
         
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = jTable1.getSelectedRow();
           
           if (selectedRowIndex<0) {
                JOptionPane.showMessageDialog(this, "Please select a row to view.");
                return;
                
           }
           
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           CarProfile selectedCarProfile = (CarProfile) model.getValueAt(selectedRowIndex , 0);
           
           txtBrand.setText(selectedCarProfile.getBrand());
           txtModel.setText(selectedCarProfile.getModel());
           txtColor.setText(selectedCarProfile.getColor());
           txtYear.setText(String.valueOf(selectedCarProfile.getYear()));
           txtEngineNumber.setText(selectedCarProfile.getEngineNumber());
           txtNumberofSeats.setText(String.valueOf(selectedCarProfile.getNumberofSeats()));
           txtLicensePlate.setText(selectedCarProfile.getLicensePlates());
           txtOwnerName.setText(selectedCarProfile.getOwnerName());
           txtOwnerTelephoneNumber.setText(String.valueOf(selectedCarProfile.getOwnerTelephoneNumber()));
           txtOwnerEmailAddress.setText(selectedCarProfile.getOwnerEmailAddress());
           txtOwnerDriverLicense.setText(selectedCarProfile.getOwnerDriverLicense());
           txtOwnerSocialSecurityNumber.setText(String.valueOf(selectedCarProfile.getOwnerSocialSecurityNumber()));
           txtOwnerAddress.setText(selectedCarProfile.getOwnerAddress());
           txtWarrantyYear.setText(String.valueOf(selectedCarProfile.getWarrantyYear()));
           ImageIcon icon = new ImageIcon((selectedCarProfile.getImage()));
           lblImage.setIcon(icon);
           
           populateTable1();
        
        
        
        
    }//GEN-LAST:event_btnViewActionPerformed


