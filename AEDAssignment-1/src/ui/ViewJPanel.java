    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        for (CarProfile cp : history.getHistory()){
            
            Object[] row = new Object[4];
            row[0] = cp;
            row[1] = cp.getModel();
            row[2] = cp.getColor();
            row[3] = cp.getYear();
            
            model.addRow(row);
            
            
        }
        
        
    }
    
