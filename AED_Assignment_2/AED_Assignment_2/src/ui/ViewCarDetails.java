    private void populateUber() {
        //To change body of generated methods, choose Tools | Templates.
        txtBrand.setText(ubercars.getBrand());
        txtYearofManufacture.setText(String.valueOf(ubercars.getYearofManufacture()));
        txtMaxSeats.setText(String.valueOf(ubercars.getMaxSeats()));
        txtMinSeats.setText(String.valueOf(ubercars.getMinSeats()));
        txtSerialNumber.setText(String.valueOf(ubercars.getSerialNumber()));
        txtModelNumber.setText(ubercars.getModelNumber());
        txtCity.setText(ubercars.getAvailableCity());
        if(ubercars.isAvailability() == true)
        {
            btnTrue.setEnabled(false);
            btnFalse.setEnabled(false);
            btnTrue.setSelected(true);
        }
        else
        {
            btnTrue.setEnabled(false);
            btnFalse.setEnabled(false);
            btnFalse.setSelected(true);
        }
        if(ubercars.isMaintainanceCertificate()== true)
        {
            btnExp.setEnabled(false);
            btnNonExp.setEnabled(false);
            btnExp.setSelected(true);   
        }
        else
        {
            btnExp.setEnabled(false);
            btnNonExp.setEnabled(false);
            btnNonExp.setSelected(true);
        }
         
    }


