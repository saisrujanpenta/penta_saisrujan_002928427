    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String brandname = txtBrand.getText();
        if(brandname == null || brandname.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter the Brand name");
                return;
            }
        
        String manuYear = txtYearofManufacture.getText();
        if (manuYear == null || manuYear.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Car's Year of Manufacture", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtYearofManufacture.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Enter a valid value", " Warning", JOptionPane.WARNING_MESSAGE);
        }   
        
         String maxCapa = txtMaxSeats.getText();
        if (maxCapa == null || maxCapa.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Maximum Seat Capacity", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtMaxSeats.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Enter a valid value", " Warning", JOptionPane.WARNING_MESSAGE);
        }   
         
          String minCapa = txtMinSeats.getText();
        if (minCapa == null || minCapa.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Minimum Seat Capacity", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtMinSeats.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"You need to put valid value", " Warning", JOptionPane.WARNING_MESSAGE);
        } 
         
         String serNum = txtSerialNumber.getText();
        if (serNum == null || serNum.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Serial Number", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtSerialNumber.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Enter a valid value", " Warning", JOptionPane.WARNING_MESSAGE);
        } 
         
         String modNum = txtModelNumber.getText();
        if (modNum == null || modNum.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Model Number", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         
         String avaCity = txtCity.getText();
        if (avaCity == null || avaCity.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Enter the Model Number", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        
        
        
        
        
        
        
        ubercars.setBrand(txtBrand.getText());
        ubercars.setYearofManufacture(Integer.parseInt(txtYearofManufacture.getText()));
        ubercars.setMaxSeats(Integer.parseInt(txtMaxSeats.getText()));
        ubercars.setMinSeats(Integer.parseInt(txtMinSeats.getText()));
        ubercars.setSerialNumber(Integer.parseInt(txtSerialNumber.getText()));
        ubercars.setModelNumber(txtModelNumber.getText());
        ubercars.setAvailableCity(txtCity.getText());
        if(btnTrue.isSelected())
        {
            ubercars.setAvailability(true);
        }
        else{ubercars.setAvailability(false);
        }
        
        if(btnExp.isSelected()){
            ubercars.setMaintainanceCertificate(true);
        }
        else{
         ubercars.setMaintainanceCertificate(false);
        }
        
        
        SimpleDateFormat date = new SimpleDateFormat("MM/dd/yyyy, hh:mm:ss");
        Date s = new Date();
        txtTimestamp.setText(date.format(s));
        
        JOptionPane.showMessageDialog(null, "Details updated Successfully");
       
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBack5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack5ActionPerformed
        // TODO add your handling code here:
       

        this.SelectActionPanel.remove(this);
        CardLayout layout = (CardLayout) this.SelectActionPanel.getLayout();
        
        Component[] comps = this.SelectActionPanel.getComponents();
        for (Component comp : comps)
        {
            if (comp instanceof ManageCarDetails){
                ManageCarDetails mngp = (ManageCarDetails) comp;
                mngp.populateTable(CarCatalog,"all");
            }
        }
        Component[] compss = this.SelectActionPanel.getComponents();
        for (Component comp : compss)
        {
            if (comp instanceof FilteredResultsTable){
                FilteredResultsTable mngp = (FilteredResultsTable) comp;
                mngp.populateTable(CarCatalog,"all");
            }
        }
        
        layout.previous(SelectActionPanel);
    }//GEN-LAST:event_btnBack5ActionPerformed

